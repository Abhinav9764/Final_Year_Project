# CHATBOT - RAG + SLM Flask App
# ---------------------------------------------------------------------------
FLASK_APP_CHATBOT = '''"""
Chatbot Flask app generated by RAD-ML fallback.
"""
from __future__ import annotations

import os
import sys
from pathlib import Path
from typing import Optional

from flask import Flask, jsonify, render_template_string, request

ROOT = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(ROOT))
os.chdir(ROOT)

import yaml

with open(ROOT / "config.yaml", encoding="utf-8") as f:
    CFG = yaml.safe_load(f)

app = Flask(__name__)
_RAG_INSTANCE = None


def get_rag():
    """Lazy load RAG builder so app import stays lightweight for tests."""
    global _RAG_INSTANCE
    if _RAG_INSTANCE is not None:
        return _RAG_INSTANCE

    try:
        from engines.chatbot_engine.rag_builder import RAGBuilder
        rag = RAGBuilder(CFG)
        if not rag.load_existing_index():
            return None
        _RAG_INSTANCE = rag
        return _RAG_INSTANCE
    except Exception:
        return None


@app.get("/")
def home():
    return render_template_string(
        """
        <html>
        <head><title>RAD-ML Chatbot</title></head>
        <body>
          <h2>RAD-ML Chatbot (Fallback Flask Mode)</h2>
          <form method="post" action="/api/chat">
            <input type="text" name="question" placeholder="Ask a question" style="width: 360px;" />
            <button type="submit">Ask</button>
          </form>
        </body>
        </html>
        """
    )


@app.post("/api/chat")
def chat():
    payload = request.get_json(silent=True) or {}
    question = str(payload.get("question") or request.form.get("question") or "").strip()
    if not question:
        return jsonify({"ok": False, "error": "Question is required."}), 400

    rag = get_rag()
    if rag is None:
        return jsonify({
            "ok": True,
            "answer": "RAG index is unavailable. Run chatbot collection/indexing first."
        })

    try:
        answer = str(rag.query(question)).strip()
        if not answer:
            answer = "No confident answer could be generated."
        return jsonify({"ok": True, "answer": answer})
    except Exception as exc:
        return jsonify({"ok": False, "error": str(exc)}), 500


if __name__ == "__main__":
    flask_cfg = CFG.get("flask", {})
    host = str(flask_cfg.get("host", "0.0.0.0"))
    port = int(flask_cfg.get("port", 5000))
    debug = bool(flask_cfg.get("debug", False))
    app.run(host=host, port=port, debug=debug)
'''

