# ── Lazy RAG loader ───────────────────────────────────────────────────────────
@st.cache_resource(show_spinner="Loading knowledge base…")
def get_rag():
    """Load the pre-built Chroma RAG index on first request."""
    try:
        from engines.chatbot_engine.rag_builder import RAGBuilder
        rag = RAGBuilder(CFG)
        if rag.load_existing_index():
            return rag
        return None
    except Exception as exc:
        st.warning(f"RAG index unavailable: {exc}")
        return None


def generate_answer(question: str) -> str:
    """Query the RAG pipeline and return a string answer."""
    question = str(question or "").strip()
    if not question:
        return "Please enter a valid question."

    rag = get_rag()
    if rag is None:
        return "Knowledge base is unavailable. Run data collection and indexing first."

    try:
        answer = str(rag.query(question)).strip()
        return answer if answer and answer != "None" else "No confident answer could be generated."
    except Exception as exc:
        return f"Inference error: {exc}"


