AWS_REGION = CFG.get("aws", {}).get("region", "us-east-1")
REMOTE_INFERENCE_ENABLED = os.environ.get("SAGEMAKER_REMOTE_ENABLED", "1") == "1"
MOCK_MODE = os.environ.get("RADML_MOCK_MODE", "0") == "1" or not REMOTE_INFERENCE_ENABLED
