def get_sm_runtime():
    """Create the SageMaker runtime client lazily to avoid startup stalls."""
    if not boto3 or not REMOTE_INFERENCE_ENABLED:
        return None
    try:
        return boto3.client("sagemaker-runtime", region_name=AWS_REGION)
    except Exception:
        return None

# ── Core logic (kept pure for unit-testability) ───────────────────────────────
def _encode_feature(feature_name: str, value: str) -> str:
    """Strip units/symbols and encode a single feature value as a numeric string."""
    value = str(value).strip()
    # Strip all non-numeric characters except decimal point
    cleaned = re.sub(r"[^0-9.]", "", value)

    if feature_name.lower() in ("region", "location", "area_name", "city", "state", "text", "description", "review"):
        # Ordinal hash encoding for categorical fields
        return str(sum(ord(ch) for ch in value.lower()) % 1000)

    try:
        return str(float(cleaned)) if cleaned else "0.0"
    except ValueError:
        return str(sum(ord(ch) for ch in value.lower()) % 1000)


def _is_numeric(value: str) -> bool:
    try:
        float(str(value).strip())
        return True
    except Exception:
        return False


def _local_fallback_prediction(values_list: List[str]) -> str:
    nums = [float(v) for v in values_list if _is_numeric(v)]
    if not nums:
        return "0.0"
    return str(round(sum(nums) / max(len(nums), 1), 3))


def _is_recoverable_endpoint_error(exc: Exception) -> bool:
    text = str(exc).lower()
    recoverable_tokens = (
        "could not connect to the endpoint url",
        "validationerror",
        "could not find endpoint",
        "endpoint",
        "not found",
        "does not exist",
    )
    return any(token in text for token in recoverable_tokens)


def validate_payload(payload: Dict[str, str]) -> Tuple[bool, str]:
    missing = [f for f in FEATURES if not str(payload.get(f, "")).strip()]
    if missing:
        return False, f"Missing required fields: {', '.join(missing)}"
    geo_fields = {"region", "location", "area_name", "city", "state"}
    for gf in geo_fields:
        if gf in FEATURES and not str(payload.get(gf, "")).strip():
            return False, f"'{gf}' is required for location-specific prediction."
    return True, ""


def make_prediction(values_list: List[str]) -> str:
    payload_str = ",".join(values_list)
    sm_runtime = get_sm_runtime()
    if sm_runtime is None or MOCK_MODE:
        return _local_fallback_prediction(values_list)
    try:
        response = sm_runtime.invoke_endpoint(
            EndpointName=ENDPOINT_NAME,
            ContentType="text/csv",
            Body=payload_str,
        )
        return response["Body"].read().decode("utf-8").strip()
    except Exception as exc:
        if _is_recoverable_endpoint_error(exc):
            return _local_fallback_prediction(values_list)
        raise RuntimeError(f"SageMaker endpoint error: {exc}") from exc


def predict_record(record: Dict[str, str]) -> str:
    encoded = [_encode_feature(f, record.get(f, "")) for f in FEATURES]
    return make_prediction(encoded)


